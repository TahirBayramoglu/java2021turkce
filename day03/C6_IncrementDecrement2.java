package day03;

public class C6_IncrementDecrement2 {
	
	public static void main(String[] args) {
		
		int number =10;
		//bi variable in degerini toplayarak artirmak icin 3 yöntem kullanabilirim
		
		//1. yöntem
		
		number = number + 5;
		
		// 2. yöntem 
		
		number +=8;
		
		//3.yöntem sadece 1(bir) artirmak icin. Bu yönten sadece + ve - islemleri icin gecerlidir
		
		number++;
		
		
		
		//number i 6 eksiltin
		
		number-=6;
		
		//number i ikiye bölun
		
		number /=2;
		
		//number i 3 le carpin
		
		number *=3;
		
		//number i 1 eksiltin
		
		number --;
		
	}	

}
